	<blockquote>
<p>
It sucks. It's unplayable, dull, and less interesting than drying paint:
</p>
<pre>
[...]

A chair appears out of nowhere!

What magnificent beauty! You reach out to the chair...
...and the chair bursts into flames.
You die...
</pre>
<p>
Let's consider how much user interaction I've had so far: I typed in my name, selected a role, pressed a number, and instantly died.
</p>
<p>
The built-in delays are stupid and a waste of my time.
</p>
<p>
Deaths should be the player's fault. That is, I should not choose to take one path and be instantly cremated by a flaming chair. It's like if you took your first step in NetHack, stepped on a trapdoor, fell down, triggered a rolling boulder trap, the boulder sets off a landmine, and causes the dungeon to collapse, killing you, while watching the whole debacle in slow motion.
</p>
<p>
Since everything is totally random, there is literally nothing I can do. Surviving is not a matter of player skill. A random number generator has a better chance at winning than I do.
</p>
<p>
The only motivation to play is to see if I can find all of the messages and random ways of dying. But given that each iteration of the game is painfully slow, and I've come home from a day at work, and there are better things to do, I simply put aside Jadventure.
</p>
<p>
Better luck next time.
</p>
		<footer>Anonymous, June 2016</footer> <!-- or maybe Anon Y. Mous... ;D -->
	</blockquote>
