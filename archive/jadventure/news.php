	<h4>JAdventure 2.0 stable release!</h4>
	<div class="date">December 8th, 2016</div>
	<br>
	<p>
	After months of vicious coding and suspense, the JAdventure development team is proud to announce the 2.0 stable release! As the version number suggests, 2.0 is a massive overhaul of anything and everything. Vast swaths of bugs were finally squashed and plenty of shiny new features were squeaked in. Innumerable tweaks to the user interface assuages eyesores, and a brand-new commands system has been introduced to increase the interface flexibility. Font colors and ASCII art have also been introduced to JAdventure, and extra flavor messages were added everywhere. Combat has not been neglected either, and new improvements include bare-handed combat and an ammunition system for the crossbow. Read the <a href="ChangeLog.txt">ChangeLog</a> for a complete list of changes and download the release <a href="versions/jadventure-2.0.zip">here</a>.
	</p>
	<br>

	<h4>Website update</h4>
	<div class="date">October 30th, 2016</div>
	<br>
	<p>
	After four months of neglect, the JAdventure website has finally received a much-needed overhaul. Rather than have everything cluttered into one page, important information is now contained in separate tabs. The news archive is also merged with the home page, and a brief introduction is given to accompany the lone screenshot rather than have a redundant news post. The <a href="about.php">About</a> section now includes a brief overlay of notable features in JAdventure, providing readers a quick and painless way to analyze the completeness of the game. In the <a href="download.php">Download</a> section, a brief tutorial on installing JAdventure and its dependencies is also created, further reducing the pain required to install the game. Finally, portions of the reviews that the development team left out in previous iterations of the website are now added in order to give a more accurate portrayal of JAdventure by our players. You can submit reviews of your own by email, and you may also request for your reviews to be displayed anonymously on our website.
	</p>
