	<h4>JAdventure 1.4 release!</h4>
	<div class="date">July 19th, 2016</div>
	<br>
	<p>
	The JAdventure development team is proud to announce the 1.4 release. This particular release includes massive changes and most likely is the last in the 1.x series. Among the new features are random map generation and increased monster variety, as well as the usual plethora of bug fixes. Read the <a href="ChangeLog.txt">ChangeLog</a> for details and download the release <a href="versions/jadventure-1.4.zip">here</a>.</p>
	<p>While preparing the 1.4 release, the development team noticed that introducing new features became more and more difficult, and bugs began requiring more and more effort to find and squash. The difficulties were increasing because of JAdventure's poor code structure, and therefore the development team has decided to completely rewrite JAdventure from scratch. Naturally this will take time, a <i>long</i> time, and so it is safe to say the 1.4 release is the last of this summer.
	</p>
